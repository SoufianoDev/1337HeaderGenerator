public class FtPrintAlphabet {

    public static void ft_print_alphabet() {
        char alpha = 'a';
        while (alpha <= 'z') {
            try {
                System.out.write(alpha);
            } catch (Exception e) {
                e.printStackTrace();
            }
            alpha++;
        }
        System.out.flush();
    }
}
